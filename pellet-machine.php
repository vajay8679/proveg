<div class="rgt-content">
<p class="text-justify" style="padding-top:10px;"><strong>Process:</strong> The dried and granulated raw-material is fed into the machine with the help of
in-feed screw conveyor. Further, the material enters into the machine side feeders which pushes the material into the briquetting chamber. The Bio coal is formed in the same chamber without the need of any binder with high pressure mechanical punch.</p>
<p class="text-justify"><strong>Use:</strong> DualFeed<sup>TM</sup> Briquette Machine is used to make both Biomass Briquettes and Biomass Pellets from the same machine without any need of binder or adhesive. It terms to be the best replacement of black coal &amp; other fossil fuels. The Briquettes and the Pellets of size
8mm/ 10mm/ 20mm/ 70mm/ 90mm can be made.</p>
<p class="text-justify"><strong>In-put raw materials:</strong> Any agricultural or forest waste in granulated form with moisture content from 8-12% can be used for making Biomass Briquettes and Biomass Pellets.</p>
<p class="text-justify"><span class="eco-red"><strong><u>Raw Material Types:</u></strong></span></p>
<p class="text-justify"><strong>Forest waste and residues:</strong> Saw Dust, Sandar Dust, Wood chips &amp; Shavings, Tree bark and Twigs, Pine needles, Wild grasses, Shrubs and Bamboo leaves, Bamboo dust, Viner waste, Wood peeling waste etc.</p>
<p class="text-justify"><strong>Agricultural Waste and residues:</strong> Rice husk, Sunflower, Groundnut shells, Almonds shells, Coconut shells, Cotton stalks, Arhar stalks, Sugar cane leaves and trash, Mustard stalks, Bajra cobs, Coir dust, Paddy straw, Wheat straw and Mentha Plant waste, Maize plant waste, Corn cob waste, Coffee waste, Tea waste etc.</p>
<p class="text-justify"><strong>Other Waste:</strong> Press mud, Bagasse, MSW, RDF, Paper waste, Coffee ground waste, etc.</p>
<p class="text-justify"><strong>Vernacular Names:</strong> Briquette Press, Mud Press, Screw Briquetting Machine, Jumbo Briquette Machine, Sawdust Briquette Machine, Rice Husk Briquette Machine, Biomass Briquette Plant, Biomass Briquette Machine, Gulla Machine, Pellet Machine, Pellet Mill,
Combo Machine, White Coal Making Machine, Biocoal Making Machine etc.</p>
